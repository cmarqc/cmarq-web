export type PhotoCategory = 'landscape' | 'portrait' | 'nature' | 'street' | 'urban' | 'all'

export interface Photo {
  id: string
  src: string
  thumbnail: string
  title: string
  location?: string
  camera?: string
  width: number
  height: number
  featured: boolean
  category: Exclude<PhotoCategory, 'all'>[]
  collection: string
  /**
   * Optional finer grouping within a collection (e.g. "Mount Sinai" inside the
   * "Egypt" collection). Drives the gallery's sub-collection filter row;
   * purchasing still happens at the collection level, so this is display-only.
   */
  subcollection?: string
  /**
   * The matching Instagram post. When set and INSTAGRAM_ACCESS_TOKEN is
   * configured, the photo's stats include that post's Instagram likes.
   *
   * Accepts a bare shortcode ("C1Dj4PJpn8U"), a shortcode with a carousel
   * image index ("C1Dj4PJpn8U?img_index=2"), or a full post URL
   * ("https://www.instagram.com/p/C1Dj4PJpn8U/?img_index=2"). The image index
   * only deep-links the "likes on Instagram" link to that slide — Instagram
   * reports likes per post, not per carousel image.
   */
  instagram?: string
}

